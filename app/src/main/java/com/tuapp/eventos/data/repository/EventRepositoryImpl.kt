class EventRepositoryImpl : EventRepository {
    
    private val client = SupabaseModule.client

    override fun getEvents(): Flow<List<Event>> = flow {
        try {
            val events = client.from("events")
                .select()
                .decodeList<Event>()
            emit(events)
        } catch (e: Exception) {
            android.util.Log.e("EventRepository", "Error fetching events: ${e.message}")
            emit(emptyList())
        }
    }

    override suspend fun getEventById(id: String): Event? {
        return try {
            client.from("events")
                .select {
                    filter {
                        eq("id", id)
                    }
                }
                .decodeSingle<Event>()
        } catch (e: Exception) {
            null
        }
    }

    override suspend fun createEvent(event: Event, roles: List<Role>): Result<String> {
        return try {
            android.util.Log.d("EventRepository", "Creating event: $event")
            
            // Convert to JSON and remove fields that should be generated by DB
            val eventJson = Json.encodeToJsonElement(Event.serializer(), event).jsonObject.toMutableMap()
            eventJson.remove("id")
            eventJson.remove("created_at")
            eventJson.remove("updated_at")
            
            // Filter out any remaining nulls to avoid constraint violations
            val filteredJson = eventJson.filterValues { it !is kotlinx.serialization.json.JsonNull }
            
            val insertedEvent = client.from("events")
                .insert(filteredJson) {
                    select()
                }
                .decodeSingle<Event>()
            
            val eventId = insertedEvent.id ?: throw Exception("Failed to get event ID")

            if (roles.isNotEmpty()) {
                val rolesToInsert = roles.map { role ->
                    val roleJson = Json.encodeToJsonElement(Role.serializer(), role.copy(eventId = eventId)).jsonObject.toMutableMap()
                    roleJson.remove("id")
                    roleJson.remove("created_at")
                    roleJson.filterValues { it !is kotlinx.serialization.json.JsonNull }
                }
                client.from("event_roles").insert(rolesToInsert)
            }

            val ownerMember = EventMember(
                eventId = eventId,
                userId = event.createdBy,
                isAdmin = true,
                status = "active"
            )
            
            // Clean member JSON as well
            val ownerJson = Json.encodeToJsonElement(EventMember.serializer(), ownerMember).jsonObject.toMutableMap()
            ownerJson.remove("joined_at")
            ownerJson.remove("created_at")
            
            client.from("event_members").insert(ownerJson.filterValues { it !is kotlinx.serialization.json.JsonNull })

            Result.success(eventId)
        } catch (e: Exception) {
            android.util.Log.e("EventRepository", "Error creating event: ${e.message}", e)
            Result.failure(e)
        }
    }

    override suspend fun updateEvent(event: Event): Result<Unit> {
        return try {
            client.from("events").update(event) {
