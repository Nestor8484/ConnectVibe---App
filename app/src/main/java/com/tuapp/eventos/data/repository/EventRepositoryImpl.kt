    override suspend fun getEventParticipants(eventId: String): Result<List<GroupMember>> {
        return try {
            val response = client.from("event_members")
                .select(Columns.raw("*, profiles(*)")) {
                    filter {
                        eq("event_id", eventId)
                    }
                }
            
            val jsonElement = Json.parseToJsonElement(response.data)
            if (jsonElement !is kotlinx.serialization.json.JsonArray) {
                return Result.success(emptyList())
            }
            
            val participants = jsonElement.mapNotNull { element ->
                try {
                    val obj = element.jsonObject
                    val userId = obj["user_id"]?.jsonPrimitive?.contentOrNull ?: return@mapNotNull null
                    val isAdmin = obj["is_admin"]?.jsonPrimitive?.let { 
                        it.booleanOrNull ?: (it.content == "true") || (it.content == "1")
                    } ?: false
                    
                    // Manejar que profiles pueda venir como objeto o como lista (Join 1:1 vs 1:N)
                    val profilesElement = obj["profiles"]
                    val profileObj = when (profilesElement) {
                        is kotlinx.serialization.json.JsonObject -> profilesElement
                        is kotlinx.serialization.json.JsonArray -> if (profilesElement.isNotEmpty()) profilesElement[0].jsonObject else null
                        else -> null
                    }
                    
                    val fullName = profileObj?.get("full_name")?.jsonPrimitive?.contentOrNull ?: "Usuario"
                    val username = profileObj?.get("username")?.jsonPrimitive?.contentOrNull ?: "usuario"
                    
                    GroupMember(
                        userId = userId,
                        userName = fullName,
                        role = if (isAdmin) MemberRole.ADMIN else MemberRole.MEMBER,
                        email = "@$username"
                    )
                } catch (e: Exception) {
                    android.util.Log.e("EventRepository", "Error parsing participant row: ${e.message}")
                    null
                }
            }
            
            Result.success(participants)
        } catch (e: Exception) {
            android.util.Log.e("EventRepository", "Error getting participants: ${e.message}")
            Result.failure(e)
        }
    }

    override suspend fun updateParticipantRole(eventId: String, userId: String, isAdmin: Boolean): Result<Unit> {
        return try {
            android.util.Log.d("EventRepository", "Updating role: event=$eventId, user=$userId, isAdmin=$isAdmin")
            
            // Usamos update en lugar de upsert para evitar activar múltiples políticas (INSERT y UPDATE)
            // y para que sea más eficiente cuando ya sabemos que el registro existe.
            client.from("event_members").update(
                buildJsonObject {
                    put("is_admin", isAdmin)
                }
            ) {
                filter {
                    eq("event_id", eventId)
                    eq("user_id", userId)
                }
            }
            
            android.util.Log.d("EventRepository", "Update successful")
            Result.success(Unit)
        } catch (e: Exception) {
            android.util.Log.e("EventRepository", "Error updating participant role for user $userId in event $eventId: ${e.message}", e)
            Result.failure(e)
        }
    }

