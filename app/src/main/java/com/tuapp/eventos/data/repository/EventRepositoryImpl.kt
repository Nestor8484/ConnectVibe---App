            Result.failure(e)
        }
    }

    override suspend fun joinEvent(eventId: String, userId: String): Result<Unit> {
        return try {
            android.util.Log.d("EventRepository", "Attempting to join event $eventId for user $userId")
            // First check if already member to avoid duplicate key error
            val existing = client.from("event_members")
                .select {
                    filter {
                        eq("event_id", eventId)
                        eq("user_id", userId)
                    }
                }.decodeSingleOrNull<EventMember>()

            if (existing != null) {
                android.util.Log.d("EventRepository", "User already joined event")
                return Result.success(Unit)
            }

            val member = EventMember(
                eventId = eventId,
                userId = userId,
                isAdmin = false,
                status = "active"
            )
            
            // Clean member JSON to avoid null date constraint errors
            val memberJson = Json.encodeToJsonElement(EventMember.serializer(), member).jsonObject.toMutableMap()
            memberJson.remove("joined_at")
            memberJson.remove("created_at")
            
            client.from("event_members").insert(memberJson.filterValues { it !is kotlinx.serialization.json.JsonNull })

            android.util.Log.d("EventRepository", "Successfully joined event")
            Result.success(Unit)
        } catch (e: Exception) {
            android.util.Log.e("EventRepository", "Error joining event: ${e.message}", e)
            Result.failure(e)
        }
    }

    override suspend fun leaveEvent(eventId: String, userId: String): Result<Unit> {
        return try {
            android.util.Log.d("EventRepository", "Removing user $userId from event $eventId")
            
            // 1. Eliminar al usuario de todos los roles que tenga asignados en este evento
            client.from("event_role_members").delete {
                filter {
                    eq("event_id", eventId)
                    eq("user_id", userId)
                }
            }
            
            // 2. Eliminar al usuario de la tabla de miembros del evento
            client.from("event_members").delete {
                filter {
                    eq("event_id", eventId)
                    eq("user_id", userId)
                }
            }
            
            Result.success(Unit)
        } catch (e: Exception) {
            android.util.Log.e("EventRepository", "Error removing user from event: ${e.message}", e)
            Result.failure(e)
        }
    }

